package com.omnicron.avax.helpers;

public class Statistic {
    private int vaccinated, unvaccinated, boostered;

    public int getVaccinated() {
        return vaccinated;
    }

    public void setVaccinated(int vaccinated) {
        this.vaccinated = vaccinated;
    }

    public int getUnvaccinated() {
        return unvaccinated;
    }

    public void setUnvaccinated(int unvaccinated) {
        this.unvaccinated = unvaccinated;
    }

    public int getBoostered() {
        return boostered;
    }

    public void setBoostered(int boostered) {
        this.boostered = boostered;
    }
}
